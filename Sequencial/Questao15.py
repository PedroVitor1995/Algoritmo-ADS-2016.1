"""15. Leia o valor da base e altura de um triangulo, calcule e escreva sua area. (area=(base * altura)/2)"""

base = input ('Digite o valor da base de um triangulo: ' )
altura = input ('\nDigite a altura de um triangulo: ')

area = (base * altura) / 2

print ('\nA area do triangulo e %.2f') % (area)