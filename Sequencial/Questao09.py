"""9. Leia 2 numeros (A, B) e escreva-os em ordem inversa (B, A)"""	
def main():
	A = input ('Digite o primeiro numero: ')
	B = input ('\nDigite o segundo numero: ')
	print ('\nOs numeros digitados foram %d %d') % (A,B)
	print ('\nA ordem inversa dos numeros digitados e %d %d') % (B,A)
if __name__ == '__main__':
	main()