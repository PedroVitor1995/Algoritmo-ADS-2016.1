"""21. Leia uma temperatura em Fahrenheit, calcule e escreva a equivalente em Celsius. (tCelsius = (5 * tFahrenheit - 160) / 9)."""

Fahrenheit = input ("Digite a temperatura em Fahrenheit: ")

Celsius = ((5 * Fahrenheit - 160) / 9)

print ("\nA temperatura de %.2f Fahrenheit em Celsius  e %.2f Celsius") % (Fahrenheit,Celsius,)